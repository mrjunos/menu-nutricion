export function findFood(foodId) {
  for (const [group, items] of Object.entries(FOODS)) {
    const item = items.find((f) => f.id === foodId);
    if (item) return { ...item, group };
  }
  return null;
}

export function pesajeDe(food) {
  return food.pesaje || GROUP_META[food.group]?.pesaje || 'cocido';
}
